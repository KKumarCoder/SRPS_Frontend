export const photos = photoFiles.map((file) => {
  const name = file.replace(/\\.[A-Za-z0-9]+$/, "");
  const descKey = name.replace(/[^a-zA-Z0-9]/g, "").slice(0, 12);
  const description =
    photoDescriptions[descKey] ||
    photoDescriptions[name] ||
    `${name.charAt(0).toUpperCase() + name.slice(1)} at Shree Ram School`;

  const category = file.includes("NCC")
    ? "NCC & Sports"
    : file.includes("IMG_") || file.startsWith("IMG")
      ? "Events & Activities"
      : "Campus Life";

  return { src: `/School_pic/${file}`, alt: name, category, description };
});

